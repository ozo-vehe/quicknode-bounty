let AnimeCardContract;
let signer;
let cards;

const provider = new ethers.providers.Web3Provider(window.ethereum, "goerli");

provider.send("eth_requestAccounts", []).then(() => {
  provider.listAccounts().then((accounts) => {
    signer = provider.getSigner(accounts[0]);
    AnimeCardContract = new ethers.Contract(
      ContractAddress,
      ContractABI,
      signer
    );
  });
});

async function displayCards() {
	const animeCards = document.querySelector(".animeCards");
	animeCards.innerHTML = "";
	cards.forEach((card) => {
			const newDiv = document.createElement("div")
			newDiv.className = "animeCard"
			newDiv.innerHTML = cardStored(card)
			animeCards.appendChild(newDiv)
	})
}

function cardStored(card) {
	return `
		<img id="${card.tokenId}" src="${card.url}" alt="">
	`
}

async function getNftCard() {
	const length = await AnimeCardContract.totalSupply();
	const storedCards = [];
  for (let i = 0; i < length; i++) {
    let card = new Promise(async (resolve, reject) => {
      let c = await AnimeCardContract.getCard(i)
			let url = await AnimeCardContract.tokenURI(i)
      resolve({
        tokenId: i,
        owner: c[0],
        name: c[1],
        url
      });
  	});
    storedCards.push(card)
  }
  cards = await Promise.all(storedCards)
	await displayCards();
	console.log(cards)
}

async function uploadNftCard() {
	const name = document.querySelector("#charName").value;
	const url = document.querySelector("#imgUrl").value;
	const data = [name, url]

  const uploadCardPromise = AnimeCardContract.uploadCard(...data);
  await uploadCardPromise;
	console.log("Successful")
}

document.querySelector('.createNft').addEventListener("click", async function(e) {
  e.preventDefault()
	console.log("Create Nft")
  await uploadNftCard()
})
document.querySelector('.getNft').addEventListener("click", async function(e) {
	e.preventDefault()
  console.log("Get Nft")
  await getNftCard()
})